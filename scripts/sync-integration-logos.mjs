#!/usr/bin/env node
/**
 * Sync integration logo SVGs into the comparison site marquee.
 *
 * Sources (in order):
 * 1. Local files in public/zuraio-comparison/assets/integrations/incoming/
 * 2. Remote files from Roland's reference repo (Cherrypicker77/zuraio-webseite)
 *
 * Usage:
 *   node scripts/sync-integration-logos.mjs
 *   node scripts/sync-integration-logos.mjs --from ./my-logos
 *
 * After syncing, commit the SVG files and integrations-manifest.js.
 */

import fs from 'node:fs';
import path from 'node:path';
import { fileURLToPath } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const OUT_DIR = path.join(ROOT, 'public/zuraio-comparison/assets/integrations');
const INCOMING_DIR = path.join(OUT_DIR, 'incoming');
const MANIFEST_JS = path.join(ROOT, 'public/zuraio-comparison/js/integrations-manifest.js');

/** Our id → expected output filename */
const OUTPUT_FILES = {
  outlook: 'outlook.svg',
  gmail: 'gmail.svg',
  'microsoft-365': 'microsoft-365.svg',
  'google-workspace': 'google-workspace.svg',
  teams: 'teams.svg',
  slack: 'slack.svg',
  notion: 'notion.svg',
  sharepoint: 'sharepoint.svg',
  onedrive: 'onedrive.svg',
  hubspot: 'hubspot.svg',
  salesforce: 'salesforce.svg',
  whatsapp: 'whatsapp.svg',
  'voice-notes': 'voice-notes.svg',
};

/** Alternate source filenames (Roland repo + common variants) */
const SOURCE_ALIASES = {
  outlook: ['outlook.svg'],
  gmail: ['gmail.svg'],
  'microsoft-365': ['microsoft-365.svg', 'microsoft365.svg', 'microsoft.svg'],
  'google-workspace': ['google-workspace.svg', 'googleworkspace.svg', 'google.svg'],
  teams: ['teams.svg'],
  slack: ['slack.svg'],
  notion: ['notion.svg'],
  sharepoint: ['sharepoint.svg'],
  onedrive: ['onedrive.svg'],
  hubspot: ['hubspot.svg'],
  salesforce: ['salesforce.svg'],
  whatsapp: ['whatsapp.svg'],
  'voice-notes': ['voice-notes.svg', 'voicenotes.svg', 'voice_notes.svg'],
};

const REMOTE_BASE =
  'https://raw.githubusercontent.com/Cherrypicker77/zuraio-webseite/main/assets/icons/integrations';

const REMOTE_MAP = {
  outlook: 'outlook.svg',
  gmail: 'gmail.svg',
  'microsoft-365': 'microsoft365.svg',
  'google-workspace': 'googleworkspace.svg',
  teams: 'teams.svg',
  slack: 'slack.svg',
  notion: 'notion.svg',
  sharepoint: 'sharepoint.svg',
  onedrive: 'onedrive.svg',
  hubspot: 'hubspot.svg',
  salesforce: 'salesforce.svg',
  whatsapp: 'whatsapp.svg',
  'voice-notes': 'voice-notes.svg',
};

function parseArgs() {
  const args = process.argv.slice(2);
  let fromDir = null;
  for (let i = 0; i < args.length; i++) {
    if (args[i] === '--from' && args[i + 1]) {
      fromDir = path.resolve(args[++i]);
    }
  }
  return { fromDir: fromDir ?? INCOMING_DIR };
}

async function fetchRemote(name) {
  const url = `${REMOTE_BASE}/${name}`;
  const res = await fetch(url);
  if (!res.ok) return null;
  const text = await res.text();
  if (!text.trim().startsWith('<svg')) return null;
  return text;
}

function readLocal(dir, aliases) {
  if (!fs.existsSync(dir)) return null;
  for (const alias of aliases) {
    const file = path.join(dir, alias);
    if (fs.existsSync(file)) {
      return fs.readFileSync(file, 'utf8');
    }
    const lower = path.join(dir, alias.toLowerCase());
    if (fs.existsSync(lower)) {
      return fs.readFileSync(lower, 'utf8');
    }
  }
  return null;
}

function writeManifest(available) {
  const ids = [...available].sort();
  const body = `// Auto-generated by scripts/sync-integration-logos.mjs — do not edit manually
// Re-run: npm run sync:integration-logos

export const INTEGRATION_LOGOS_SYNCED_AT = ${JSON.stringify(new Date().toISOString())};

/** Integration ids with a logo file present in assets/integrations/ */
export const INTEGRATION_LOGOS_AVAILABLE = new Set(${JSON.stringify(ids)});

export function hasIntegrationLogo(id) {
  return INTEGRATION_LOGOS_AVAILABLE.has(id);
}
`;
  fs.writeFileSync(MANIFEST_JS, body);
}

async function main() {
  const { fromDir } = parseArgs();
  fs.mkdirSync(OUT_DIR, { recursive: true });
  fs.mkdirSync(INCOMING_DIR, { recursive: true });

  const available = new Set();
  const report = [];

  for (const [id, outName] of Object.entries(OUTPUT_FILES)) {
    const outPath = path.join(OUT_DIR, outName);
    let svg = readLocal(fromDir, SOURCE_ALIASES[id] ?? [outName]);
    let source = svg ? `local:${fromDir}` : null;

    if (!svg && fs.existsSync(outPath)) {
      svg = fs.readFileSync(outPath, 'utf8');
      source = 'existing';
    }

    if (!svg && REMOTE_MAP[id]) {
      svg = await fetchRemote(REMOTE_MAP[id]);
      source = svg ? 'remote:roland-reference' : null;
    }

    if (svg) {
      fs.writeFileSync(outPath, svg.trim() + '\n');
      available.add(id);
      report.push(`✓ ${id} → ${outName} (${source})`);
    } else {
      report.push(`✗ ${id} — missing (drop ${outName} into assets/integrations/incoming/)`);
    }
  }

  writeManifest(available);

  console.log('Integration logo sync complete\n');
  report.forEach((line) => console.log(line));
  console.log(`\n${available.size}/${Object.keys(OUTPUT_FILES).length} logos available`);
  console.log(`Manifest: ${path.relative(ROOT, MANIFEST_JS)}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
